"use client";

import { useForm } from "@tanstack/react-form";
import type { ZodObject, ZodTypeAny } from "zod";
import type { VexField } from "@vexcms/core";
import {
  TextField,
  NumberField,
  CheckboxFieldForm,
  SelectField,
  DateField,
  ImageUrlField,
  MultiSelectField,
  UploadField,
} from "./fields";
import type { MediaDocument } from "../ui/media-picker";

interface MediaPickerState {
  results: MediaDocument[];
  searchTerm: string;
  setSearchTerm: (s: string) => void;
  canLoadMore: boolean;
  loadMore: () => void;
  isLoading: boolean;
  selectedMedia: MediaDocument | null;
}

interface FieldEntry {
  /** Field key name (e.g., "title", "status") */
  name: string;
  /** The VexField definition */
  field: VexField;
}

interface AppFormProps {
  /** The Zod schema generated by generateFormSchema */
  schema: ZodObject<Record<string, ZodTypeAny>>;
  /** Ordered list of field entries to render */
  fieldEntries: FieldEntry[];
  /** Current document values (used as default/initial values) */
  defaultValues: Record<string, unknown>;
  /**
   * Called on valid form submit with only the changed fields.
   * Return a promise — the form shows a loading state while it resolves.
   */
  onSubmit: (changedFields: Record<string, unknown>) => Promise<void>;
  /** Optional form ID so an external button can submit via form="id" */
  formId?: string;
  /** Map of fieldName → MediaPickerState for upload fields */
  uploadFieldStates?: Record<string, MediaPickerState>;
  /** Called when an upload field's "Upload new" button is clicked */
  onOpenUploadModal?: (fieldName: string, collectionSlug: string) => void;
  /**
   * Custom renderer for upload fields.
   * When provided, this is used instead of the built-in UploadField component.
   * Allows the parent to wrap each upload field with its own hook-based state.
   */
  renderUploadField?: (props: {
    field: any;
    meta: any;
    name: string;
    onUploadNew: () => void;
    defaultValue: unknown;
  }) => React.ReactNode;
}

function AppForm({
  schema,
  fieldEntries,
  defaultValues,
  onSubmit,
  formId,
  uploadFieldStates,
  onOpenUploadModal,
  renderUploadField,
}: AppFormProps) {
  const form = useForm({
    defaultValues: defaultValues as Record<string, any>,
    validators: {
      onSubmit: schema,
    },
    onSubmit: async ({ value }) => {
      const changedFields: Record<string, unknown> = {};
      for (const entry of fieldEntries) {
        const current = value[entry.name];
        const original = defaultValues[entry.name];
        if (current !== original) {
          changedFields[entry.name] = current;
        }
      }
      if (Object.keys(changedFields).length === 0) return;
      await onSubmit(changedFields);
    },
  });

  return (
    <form
      id={formId}
      onSubmit={(e) => {
        e.preventDefault();
        e.stopPropagation();
        form.handleSubmit();
      }}
    >
      <div className="space-y-6">
        {fieldEntries.map((entry) => (
          <form.Field key={entry.name} name={entry.name}>
            {(field) => {
              const meta = entry.field._meta;

              switch (meta.type) {
                case "text":
                  return (
                    <TextField field={field} meta={meta} name={entry.name} />
                  );
                case "number":
                  return (
                    <NumberField field={field} meta={meta} name={entry.name} />
                  );
                case "checkbox":
                  return (
                    <CheckboxFieldForm
                      field={field}
                      meta={meta}
                      name={entry.name}
                    />
                  );
                case "select":
                  if (meta.hasMany) {
                    return (
                      <MultiSelectField
                        field={field}
                        meta={meta}
                        name={entry.name}
                      />
                    );
                  }
                  return (
                    <SelectField field={field} meta={meta} name={entry.name} />
                  );
                case "date":
                  return (
                    <DateField field={field} meta={meta} name={entry.name} />
                  );
                case "imageUrl":
                  return (
                    <ImageUrlField
                      field={field}
                      meta={meta}
                      name={entry.name}
                    />
                  );
                case "upload": {
                  const uploadNew = () =>
                    onOpenUploadModal?.(entry.name, meta.to);

                  if (renderUploadField) {
                    return renderUploadField({
                      field,
                      meta,
                      name: entry.name,
                      onUploadNew: uploadNew,
                      defaultValue: defaultValues[entry.name],
                    });
                  }

                  const pickerState = uploadFieldStates?.[entry.name];
                  return (
                    <UploadField
                      field={field}
                      meta={meta}
                      name={entry.name}
                      mediaResults={pickerState?.results ?? []}
                      searchTerm={pickerState?.searchTerm ?? ""}
                      onSearchChange={pickerState?.setSearchTerm ?? (() => {})}
                      canLoadMore={pickerState?.canLoadMore ?? false}
                      onLoadMore={pickerState?.loadMore ?? (() => {})}
                      isLoading={pickerState?.isLoading ?? false}
                      onUploadNew={uploadNew}
                      selectedMedia={pickerState?.selectedMedia}
                    />
                  );
                }
                default:
                  return null;
              }
            }}
          </form.Field>
        ))}
      </div>
    </form>
  );
}

export { AppForm, type AppFormProps, type FieldEntry, type MediaPickerState };
